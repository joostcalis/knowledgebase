def clear_db
  puts 'clearing database'
  Article.destroy_all
  Category.destroy_all
  Tag.destroy_all
end

def create_tags
  ['Trends', 'Ingestion', 'Quality Control', 'API'].each do |tag_title|
    Tag.create!(title: tag_title)
  end
end

def create_categories
  ['Trend Reports', 'WhiteLabel', 'Muse API', 'DDEX', 'XML', 'Bulk'].each do |category_title|
    Category.create!(title: category_title)
  end
end

